package com.example.clinica_odontologica_2023.domain;

public class Odontologo {

    private Integer id;
    private String apellido;
    private String nombre;
    private String matricula;

    public Odontologo(){}

    public Odontologo(Integer id, String apellido, String nombre, String matricula) {
        this.id = id;
        this.apellido = apellido;
        this.nombre = nombre;
        this.matricula = matricula;
    }

    public Odontologo(String apellido, String nombre, String matricula) {
        this.apellido = apellido;
        this.nombre = nombre;
        this.matricula = matricula;
    }

    //getters y setters

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String toString(){
        return "\nOdontologo { Id = "+id+", Apellido = "+apellido+", Nombre = "+nombre+", Matricula = "+matricula+" }";
    }
}
